package buffer

const (
	// Len is how long the Buffer is
	Len = 4096

	// ExtendLen is how long the length will extend for
	// Extended buffer
	ExtendLen = 16
)

// Buffer is a sequence of consecutive memory that
// will be use for temporary data storage
type Buffer [Len]byte

// Extended is same as Buffer but a bit larger
type Extended [Len + ExtendLen]byte
